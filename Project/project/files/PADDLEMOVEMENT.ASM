.MODEL SMALL
.STACK 0100H
.DATA
S1 DW 20
S2 DW 20
COLOUR DB 5
STX DW 20
COUNTER DW 0
POINT DW 50
.CODE
MOV AX,@DATA
MOV DS,AX
MOV AX, 0
MOV AL, 13H
INT 10H

L01:
;BACKGROUND CODE 
	MOV AH, 06H
	MOV AL, 0
	MOV BH, 14H
	MOV CX,0
	MOV DX, 6500
	INT 10H

L0:
	MOV BX, STX
	MOV S1, BX
	MOV BX, 0
L1:
	MOV AH, 0CH
	MOV AL, COLOUR
	MOV CX, S1
	MOV DX, S2
	INT 10H
	INC S1
	INC BX
	CMP BX, 50 ; LENGTH OF PADDLE
JE L2
JNE L1

L2:
	INC S2
	CMP S2, 25; WIDTH OF PADDLE
	JE L3
	JNE L0
L3:
	MOV S2, 20
	MOV AH, 01H
	INT 16H
JZ L0
JNZ L9

L9:
	MOV AH, 00H
	INT 16H

	CMP AL, 27
	JE EXIT

	CMP AH, 4BH
	JE LEFT

	CMP AH, 4DH
	JE RIGHT
	
	JMP L0

LEFT:

	SUB STX,4
	JMP L01

RIGHT:

	ADD STX,4
	JMP L01


EXIT::
	MOV AH,4CH
	INT 21H
	END